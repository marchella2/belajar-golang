package main

import "fmt"

func main() {
	// Tipe data number
	fmt.Println(1)
	fmt.Println(1.5) // Untuk membuat bilangan desimal menggunakan . bukan ,

	// Tipe data string
	fmt.Println("Marchella")
	fmt.Println(`Putri`) // Untuk tipe data string bisa menggunakan `` atau ""

	// Tipe data boolean
	fmt.Println(true)
	fmt.Println(false)
}
