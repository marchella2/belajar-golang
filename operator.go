package main

import "fmt"

func main() {
	// Penjumlahan
	fmt.Println(1 + 1)

	// Pengurangan
	fmt.Println(3 - 2)

	// Perkalian
	fmt.Println(5 * 2)

	// Pembagian
	fmt.Println(10 / 2)

	// Pembagian desimal
	fmt.Println(10.0 / 3.0)

	// Sisa bagi atau modulus
	fmt.Println(10 % 3)

	// Operator logika
	fmt.Println(true && false)  // && memiliki arti and
	fmt.Println(false || false) // || memiliki arti or
	fmt.Println(!false)         // ! memiliki arti not
}
